int bitwiseNot(int a) {
	return ~a;
}
int bitwiseAnd(int a, int b) {
	return a & b;
}
int bitwiseOr(int a, int b) {
	return a | b;
}
int bitwiseExclusiveOr(int a, int b) {
	return a ^ b;
}
int leftShift(int a, int b) {
	return a << b;
}
int rightShift(int a, int b) {
	return a >> b;
}
