#include <cmath>
int sum(int a, int b) {
	return a + b;
}
int sub(int a, int b) {
	return a - b;
}
int mult(int a, int b) {
	return a * b;
}
int division(int a, int b) {
	return a / b;
}
int mode(int a, int b) {
	return a % b;
}
int UnaryPlus(int a) {
	return +a;
}
int UnaryMinus(int a) {
	return-a;
}

